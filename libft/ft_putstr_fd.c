#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	len;

	i = 0;
	len = ft_strlen(s);
	if (!s)
		return ;
	while (i < len)
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
